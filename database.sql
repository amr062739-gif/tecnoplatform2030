-- 6. تفعيل الصلاحيات (Disable RLS)
-- يتم تعطيل الـ RLS حالياً لضمان عمل التطبيق مباشرة بدون تعقيدات السياسات
ALTER TABLE users DISABLE ROW LEVEL SECURITY;
ALTER TABLE content DISABLE ROW LEVEL SECURITY;
ALTER TABLE submissions DISABLE ROW LEVEL SECURITY;

-- 7. منح الصلاحيات للمستخدم المجهول (Anon Key)
-- لضمان أن مفتاح الـ Anon Key الخاص بك يستطيع القراءة والكتابة
GRANT ALL ON TABLE users TO anon;
GRANT ALL ON TABLE content TO anon;
GRANT ALL ON TABLE submissions TO anon;
GRANT ALL ON SEQUENCE users_id_seq TO anon;
GRANT ALL ON SEQUENCE content_id_seq TO anon;
GRANT ALL ON SEQUENCE submissions_id_seq TO anon;
